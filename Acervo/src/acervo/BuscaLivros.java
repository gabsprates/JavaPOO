    private void configTable() {

        this.model = (DefaultTableModel) this.listagem.getModel();
        this.listagem.getColumnModel().getColumn(this.colID).setPreferredWidth(10);
        this.listagem.getColumnModel().getColumn(this.colTitulo).setPreferredWidth(206);
        this.listagem.getColumnModel().getColumn(this.colAutor).setPreferredWidth(206);
        this.listagem.getColumnModel().getColumn(this.colDelete).setPreferredWidth(30);
        this.listagem.setRowHeight(40);

    }
    
    
    /**
     * Adiciona uma linha na listagem
     * @param livro 
     */
    private void addRowLivro(Livro livro) {
        
        this.model.addRow(new Object[]{
            livro.getId(),
            livro.getTitulo(),
            livro.getAutor(),
            false
        });
        
    }
    

    /**
     * 
     * @param termo
     * @throws SQLException 
     */
    private void buscarLivros(String termo) throws SQLException {

        this.listaLivros = livros.buscar(termo);

        this.model.setRowCount(0);

        this.listaLivros.forEach(livro -> {
            this.addRowLivro(livro);
        });

    }
    
    
