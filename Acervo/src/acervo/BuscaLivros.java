    private void apagarLivro(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_apagarLivro
        
        int row = this.listagem.getSelectedRow();
        String titulo = (String) this.listagem.getValueAt(row, this.colTitulo);
        
        if ((boolean) this.listagem.getValueAt(row, this.colDelete)) {
            
            int resp = Dialogs.confirm("Deseja apagar o livro " + titulo, "Apagar livro");
            
            if (resp == 0) {

                try {

                    this.livros.apagar((int) this.listagem.getValueAt(row, this.colID));
                    this.buscarLivros("");

                } catch (Exception ex) {
                    Dialogs.erro(ex.getMessage(), "Erro ao excluir");
                }

            } else {
                this.listagem.setValueAt(false, row, this.colDelete);
            }
        }
    }//GEN-LAST:event_apagarLivro

